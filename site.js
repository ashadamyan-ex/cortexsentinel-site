function fitCrispChatFrame(){
  const isOpen = document.body.classList.contains('crisp-chat-open');
  const layout = getCrispFrameLayout();
  const frames = getCrispCandidateFrames();
  const setFrameStyle = (frame, property, value) => {
    if (frame.style.getPropertyValue(property) !== value) {
      frame.style.setProperty(property, value, 'important');
    }
  };

  frames.forEach((frame) => {
    const rect = frame.getBoundingClientRect();
    const looksLikeLauncher = rect.width <= 130 && rect.height <= 130;

    if (isOpen && looksLikeLauncher) {
      setFrameStyle(frame, 'opacity', '0');
      setFrameStyle(frame, 'visibility', 'hidden');
      setFrameStyle(frame, 'pointer-events', 'none');
      return;
    }

    if (!isOpen && looksLikeLauncher) {
      frame.style.removeProperty('opacity');
      frame.style.removeProperty('visibility');
      frame.style.removeProperty('pointer-events');
      return;
    }

    if (!isOpen) return;

    const chatBoxNodes = [frame];
    let parent = frame.parentElement;
    while (parent && parent !== document.body && parent.id !== 'crisp-chatbox') {
      chatBoxNodes.push(parent);
      parent = parent.parentElement;
    }

    chatBoxNodes.forEach((node) => {
      setFrameStyle(node, 'position', 'fixed');
      setFrameStyle(node, 'width', `${layout.width}px`);
      setFrameStyle(node, 'height', `${layout.height}px`);
      setFrameStyle(node, 'right', `${layout.right}px`);
      setFrameStyle(node, 'bottom', `${layout.bottom}px`);
      setFrameStyle(node, 'top', 'auto');
      setFrameStyle(node, 'left', 'auto');
      setFrameStyle(node, 'max-width', `${layout.width}px`);
      setFrameStyle(node, 'max-height', `${layout.height}px`);
      setFrameStyle(node, 'min-width', '0');
      setFrameStyle(node, 'min-height', '0');
      setFrameStyle(node, 'transform', 'none');
      setFrameStyle(node, 'z-index', '2147483000');
      setFrameStyle(node, 'opacity', '1');
      setFrameStyle(node, 'visibility', 'visible');
      setFrameStyle(node, 'pointer-events', 'auto');
      setFrameStyle(node, 'overflow', 'hidden');
    });
  });

  ensureCortexCrispCloseButton(isOpen);
}

