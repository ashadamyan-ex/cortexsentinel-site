function animateBrandWord(el, nextText, removeDelay=36, addDelay=44){
  return new Promise(resolve=>{
    const currentText=el.textContent || '';
    let removeIndex=currentText.length;
    const trimStep=()=>{
      if(removeIndex>0){
        removeIndex--;
        el.textContent=currentText.slice(0,removeIndex);
        window.setTimeout(trimStep, removeDelay);
      }else{
        let addIndex=0;
        const buildStep=()=>{
          if(addIndex<nextText.length){
            addIndex++;
            el.textContent=nextText.slice(0,addIndex);
            window.setTimeout(buildStep, addDelay);
          }else{
            resolve();
          }
        };
        window.setTimeout(buildStep, 60);
      }
    };
    trimStep();
  });
}

