function normalizePrettyPath(path = '/'){
  let normalized = String(path || '/').split(/[?#]/)[0] || '/';
  if (!normalized.startsWith('/')) normalized = '/' + normalized;
  normalized = normalized.replace(/\/{2,}/g, '/');
  if (normalized.length > 1 && normalized.endsWith('/')) normalized = normalized.slice(0, -1);
  return normalized || '/';
}

function getLocaleBase(lang = currentLang){
  const localeBases = window.CORTEX_SITE_CONFIG?.localeBases || defaultLocaleBases;
  return normalizePrettyPath(localeBases[lang] || '');
}

function getSectionRoutes(){
  return window.CORTEX_SITE_CONFIG?.sectionRoutes || defaultSectionRoutes;
}

function buildLocalizedSectionPath(target = 'home', lang = currentLang){
  const route = normalizePrettyPath(getSectionRoutes()[target] || '/');
  const base = getLocaleBase(lang);
  if (route === '/') return base === '/' ? '/' : base;
  return `${base === '/' ? '' : base}${route}`;
}

function detectSectionTargetFromHref(href = ''){
  const value = String(href || '').trim();
  if (!value || value === '#') return 'home';
  if (!value.startsWith('#')) return '';
  const target = value.slice(1);
  return getSectionRoutes()[target] !== undefined ? target : '';
}

function resolveRouteTargetFromPath(pathname = window.location.pathname){
  const normalizedPath = normalizePrettyPath(pathname || '/');
  const currentBase = getLocaleBase(currentLang);
  let localPath = normalizedPath;
  if (currentBase !== '/' && normalizedPath === currentBase) {
    localPath = '/';
  } else if (currentBase !== '/' && normalizedPath.startsWith(`${currentBase}/`)) {
    localPath = normalizedPath.slice(currentBase.length) || '/';
  } else if (currentBase !== '/' && normalizedPath !== '/') {
    return null;
  }

  const routes = getSectionRoutes();
  return Object.keys(routes).find((key) => normalizePrettyPath(routes[key]) === normalizePrettyPath(localPath)) || null;
}

function getCurrentSectionRouteKey(){
  return resolveRouteTargetFromPath(window.location.pathname) || detectSectionTargetFromHref(window.location.hash) || 'home';
}

function scrollToSectionRoute(target = 'home', options = {}){
  const smooth = options.smooth !== false;
  const historyMode = options.historyMode || null;
  const section = target === 'home' ? null : document.getElementById(target);
  if (target !== 'home' && !section) return false;

  const doScroll = () => {
    if (target === 'home') {
      window.scrollTo({ top: 0, left: 0, behavior: smooth ? 'smooth' : 'auto' });
    } else {
      section.scrollIntoView({ behavior: smooth ? 'smooth' : 'auto', block: 'start' });
    }
  };

  doScroll();

  if (historyMode && window.location.protocol !== 'file:') {
    const nextPath = buildLocalizedSectionPath(target);
    const currentPath = normalizePrettyPath(window.location.pathname);
    if (historyMode === 'replace') {
      history.replaceState({ section: target }, '', nextPath);
    } else if (nextPath && currentPath !== nextPath) {
      history.pushState({ section: target }, '', nextPath);
    }
  }

  return true;
}

function syncPrettySectionLinks(){
  document.querySelectorAll('a[href]').forEach((anchor) => {
    if (!anchor.dataset.routeSource) anchor.dataset.routeSource = anchor.getAttribute('href') || '';
    const routeTarget = anchor.dataset.routeTarget || detectSectionTargetFromHref(anchor.dataset.routeSource);
    if (!routeTarget) return;

    anchor.dataset.routeTarget = routeTarget;
    const nextHref = buildLocalizedSectionPath(routeTarget);
    if (nextHref) anchor.setAttribute('href', nextHref);

    if (anchor.dataset.routeBound === 'true') return;
    anchor.dataset.routeBound = 'true';
    anchor.addEventListener('click', (event) => {
      if (event.defaultPrevented || event.button !== 0 || event.metaKey || event.ctrlKey || event.shiftKey || event.altKey) return;
      event.preventDefault();
      scrollToSectionRoute(routeTarget, { smooth: true, historyMode: 'push' });
    });
  });
}

function syncSectionRouteFromLocation(shouldReplace = true){
  const routeTarget = resolveRouteTargetFromPath(window.location.pathname);
  if (!routeTarget) return false;
  window.requestAnimationFrame(() => {
    scrollToSectionRoute(routeTarget, { smooth: false, historyMode: shouldReplace ? 'replace' : null });
  });
  return true;
}

function isReloadNavigation() {
  const navEntries = typeof performance !== 'undefined' && performance.getEntriesByType
    ? performance.getEntriesByType('navigation')
    : [];
  if (navEntries && navEntries[0] && navEntries[0].type) return navEntries[0].type === 'reload';
  return typeof performance !== 'undefined' && performance.navigation && performance.navigation.type === 1;
}

