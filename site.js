function isLeadReady(data){
  return Boolean(data.name) && data.phone.replace(/\D/g,'').length >= 8;
}

function updateLeadActionLinks(shouldFocus=false){
  const nameField=document.getElementById('leadName');
  const phoneField=document.getElementById('leadPhone');
  [nameField,phoneField].forEach(clearLeadFieldError);

  const data=getLeadFormData();
  if(!isLeadReady(data)){
    clearLeadFormStatus();
    return false;
  }

  const message=buildLeadMessage(data);
  const waUrl=`https://wa.me/${LEAD_CONTACT.whatsapp}?text=${encodeURIComponent(message)}`;
  const mailUrl=`mailto:${LEAD_CONTACT.email}?subject=${encodeURIComponent(getLeadCopy().mailSubject)}&body=${encodeURIComponent(message)}`;

  showLeadFormStatus(
    'ready',
    getTranslationText('form_ready_title','Your request is ready to send'),
    getTranslationText('form_ready_text','We have prepared your request. Continue with WhatsApp or email so it reaches us.'),
    waUrl,
    mailUrl
  );

  if(shouldFocus){
    const status=document.getElementById('leadFormStatus');
    try{ status?.focus({preventScroll:true}); }catch(_err){}
  }

  return true;
}

function handleLeadFormSubmit(event){
  event.preventDefault();
  updateLeadActionLinks(true);
}

function initLeadForm(){
  const form=document.getElementById('leadForm');
  if(!form || form.dataset.initialized==='true') return;
  form.dataset.initialized='true';

  form.querySelectorAll('input,select,textarea').forEach((field) => {
    const markTouched = () => {
      field.dataset.touched='true';
      clearLeadFieldError(field);
      updateLeadActionLinks();
    };
    field.addEventListener('input', markTouched);
    field.addEventListener('change', markTouched);
  });

  form.addEventListener('submit', handleLeadFormSubmit);
  syncLeadFormFromCalculator();
  updateLeadActionLinks();
}

